-- Count how many songs does a genre have:
SELECT GENRE, COUNT(*) AS SONG_COUNT 
FROM SONGS 
GROUP BY GENRE;

-- Calculate the average rating of songs from each artist:
SELECT S.ARTIST_ID, AVG(R.RATING) AS AVERAGE_RATING 
FROM SONGS S 
INNER JOIN USER_REVIEWS R ON S.SONG_ID = R.SONG_ID 
GROUP BY ARTIST_ID;



