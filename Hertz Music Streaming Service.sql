-- SUBQUERY

-- Retrieve the names of users who have a higher subscription cost than the average monthly cost:
SELECT NAME 
FROM USERS
WHERE USER_ID IN (
    SELECT USER_ID 
    FROM SUBSCRIPTIONS
    JOIN SUBSCRIPTION_PLANS ON SUBSCRIPTIONS.PLAN_ID = SUBSCRIPTION_PLANS.PLAN_ID
    WHERE MONTHLY_COST_IN_RS > (
        SELECT AVG(MONTHLY_COST_IN_RS) FROM SUBSCRIPTION_PLANS
    )
);

