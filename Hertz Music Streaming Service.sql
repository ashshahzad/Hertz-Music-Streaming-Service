-- GROUP FUNCTION

-- Retrieves the playlist names along with the number of songs in each playlist:
SELECT P.PLAYLIST_NAME, COUNT(PS.SONG_ID) AS SONG_COUNT
FROM PLAYLISTS P
LEFT JOIN PLAYLIST_SONGS PS ON P.PLAYLIST_ID = PS.PLAYLIST_ID
GROUP BY P.PLAYLIST_NAME;

-- Calculates the total duration of songs in each playlist:
SELECT PS.PLAYLIST_ID, SUM(S.DURATION_IN_SECONDS) AS TOTAL_DURATION
FROM PLAYLIST_SONGS PS
JOIN SONGS S ON PS.SONG_ID = S.SONG_ID
GROUP BY PS.PLAYLIST_ID;

-- Find the duration of the longest song in an album:
SELECT ALBUM_ID, MAX(DURATION_IN_SECONDS) AS MAX_DURATION
FROM SONGS
WHERE ALBUM_ID IS NOT NULL
GROUP BY ALBUM_ID
ORDER BY ALBUM_ID;

