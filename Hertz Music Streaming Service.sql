-- To show which song is in which chart on which rank:
SELECT C.CHART_ID, M.CHART_NAME, S.SONG_ID, S.SONG_TITLE, C.RANK, M.CHART_DESCRIPTION
FROM MUSIC_CHARTS M
INNER JOIN CHART_SONGS C ON C.CHART_ID = M.CHART_ID
INNER JOIN SONGS S ON C.SONG_ID = S.SONG_ID
ORDER BY C.CHART_ID, C.RANK;

